import UIKit

// Text styles
extension UIFont {
    class func dbTextStyle4Font() -> UIFont {
        return UIFont.systemFont(ofSize: 17.0, weight: UIFontWeightSemibold)
    }
    
    class func dbTextStyleFont() -> UIFont {
        return UIFont.systemFont(ofSize: 16.0, weight: UIFontWeightMedium)
    }
    
    class func dbRegularFont() -> UIFont {
        return UIFont.systemFont(ofSize: 14.0, weight: UIFontWeightRegular)
    }
    
    class func dbTextStyle2Font() -> UIFont {
        return UIFont.systemFont(ofSize: 13.0, weight: UIFontWeightMedium)
    }
    
    class func dbTextStyle5Font() -> UIFont {
        return UIFont.systemFont(ofSize: 11.0, weight: UIFontWeightMedium)
    }
    
    class func dbSfuiTextMediumFont() -> UIFont {
        return UIFont.systemFont(ofSize: 13.0, weight: UIFontWeightMedium)
    }
    
    class func dbTextStyle3Font() -> UIFont {
        return UIFont.systemFont(ofSize: 13.0, weight: UIFontWeightMedium)
    }
}
